// Function to show the popup with flip effect
function openPopup() {
  let popupOverlay = document.getElementById("popup-overlay");
  popupOverlay.style.display = "flex";
  setTimeout(() => {
    popupOverlay.classList.add("active");
  }, 50); 
}

// Function to hide the popup
function closePopup() {
  let popupOverlay = document.getElementById("popup-overlay");
  popupOverlay.classList.remove("active");
  setTimeout(() => {
    popupOverlay.style.display = "none";
  }, 500); 
}


document.addEventListener("click", function (event) {
  let clickedElement = event.target;
  let isService = clickedElement.classList.contains("service") || clickedElement.closest(".service");
  
  document.querySelectorAll(".dropdown").forEach(dropdown => {
      let parentService = dropdown.closest(".service");

      if (parentService && parentService.contains(clickedElement)) {
          dropdown.style.display = dropdown.style.display === "block" ? "none" : "block";
      } 
      else if (!isService) {
          dropdown.style.display = "none";
      }
  });
});



